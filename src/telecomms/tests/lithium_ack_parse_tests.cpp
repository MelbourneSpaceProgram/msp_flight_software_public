TEST(LithiumAckParse, TestFailNoOpAckParse) {
    byte invalid_sync_a[8] = {0x49, 0x65, 0x20, 0x01, 0x0a, 0x0a, 0x35, 0xa1};
    CHECK_FALSE(LithiumUtils::IsValidHeader(invalid_sync_a));

    byte invalid_sync_b[8] = {0x48, 0x63, 0x20, 0x01, 0x0a, 0x0a, 0x35, 0xa1};
    CHECK_FALSE(LithiumUtils::IsValidHeader(invalid_sync_b));

    byte invalid_direction[8] = {0x48, 0x65, 0x21, 0x01,
                                 0x0a, 0x0a, 0x35, 0xa1};
    CHECK_FALSE(LithiumUtils::IsValidHeader(invalid_direction));

    byte invalid_checksum[8] = {0x48, 0x65, 0x20, 0x01, 0x0a, 0x0a, 0x42, 0x42};
    CHECK_FALSE(LithiumUtils::IsValidHeader(invalid_checksum));
}

